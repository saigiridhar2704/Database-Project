            insert.setString(2, playerName);
            insert.setString(3, playerNationality);
            insert.setString(4, playerDOB);
            insert.setString(5, playerPosition);
            insert.setString(6, playerShirtName);
            insert.setString(7, playerShirtNumber);
            insert.setString(8, playerID);
            
            insert.executeUpdate();

